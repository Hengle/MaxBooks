/// <summary> 键盘按键事件类型 </summary>
public enum KeyboardEventType {

    /// <summary> 按键按下 </summary>
    Down = 0,

    /// <summary> 按键松开 </summary>
    Up = 1,

    /// <summary> 按键按住 ( 每帧派发一次 ) </summary>
    HoldDown = 2
}

