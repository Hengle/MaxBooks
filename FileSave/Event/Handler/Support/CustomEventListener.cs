using static TraceTool;
using UnityEngine;
using System;
using System.Collections.Generic;

namespace NF.Core.Event.Handler.Support {
    internal class CustomEventListener : BaseListener {

        // --- Field & Property ----------------------------------------------------------------------------------------------------------------------------------------------------- //

        // 自定义事件类型
        internal byte[] EventType { get; set; }

        // 事件侦听函数
        internal Action<object> Listener { get; set; }

        // 是否自动移除
        internal bool AutoRemove { get; set; }

        // --- Public & Protected Function ------------------------------------------------------------------------------------------------------------------------------------------ //

        // --- Internal Function ---------------------------------------------------------------------------------------------------------------------------------------------------- //

        // 派发事件 ( 重写 )
        internal override void Dispatch( params object[] args ) => Listener.Invoke( args[0] );

        // --- Private Function ----------------------------------------------------------------------------------------------------------------------------------------------------- //

    }
}

